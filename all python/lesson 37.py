def func(x):
    if x == 1:
        return x
    else:
        return x * func(x - 1) # 5 * 4 * 3 * 2
print(func(6))

# Вводим числа по одному пока не введем 0, 
def revers():
    n = int(input("введите число: "))
    if n != 0:
        revers()
        print(n)

revers()

def test(x,y):
    if y == 0:
        return 1
    else:
        return x * test(x, y - 1)  # Тут y как счетчик, а 2 умножается 2*2*2*1 если я правельно про 1 понял
